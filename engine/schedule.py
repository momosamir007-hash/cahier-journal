def get_all_teaching_subjects() -> set:
    """استخراج كل المواد التعليمية من التوقيت"""
    subjects = set()
    for day_plan in weekly_schedule.values():
        for s in day_plan:
            if s["النشاط"] not in ROUTINE_ACTIVITIES:
                subjects.add(s["النشاط"])
    return subjects


def get_domain_for(activity_name: str) -> str:
    """إرجاع اسم المجال لمادة معينة"""
    return DOMAIN_MAPPING.get(activity_name, "—")


def verify_schedule():
    """التحقق من صحة التوزيع الأسبوعي"""
    count = {}
    for day, plan in weekly_schedule.items():
        for s in plan:
            act = s["النشاط"]
            if act not in ROUTINE_ACTIVITIES:
                count[act] = count.get(act, 0) + 1

    errors = []
    for subject, expected in SUBJECT_WEEKLY_COUNT.items():
        actual = count.get(subject, 0)
        if actual != expected:
            errors.append(
                f"  {subject}: متوقع {expected} | فعلي {actual}"
            )

    return errors, count
